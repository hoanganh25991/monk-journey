/**
 * Get icon data for a skill or variant
 * @param {string} name - Name of the skill or variant
 * @returns {Object} Icon data or default icon if not found
 */
export function getSkillIcon(name) {
    return SKILL_ICONS[name] || {
        emoji: "‚ùì",
        cssClass: "icon-unknown",
        color: "#cccccc"
    };
}

/**
 * Get appropriate buff icon based on effect type
 * @param {string} effectType - Type of effect (e.g., "Increased damage")
 * @returns {Object} Icon data for the effect
 */
export function getBuffIcon(effectType) {
    const effectLower = effectType.toLowerCase();
    
    if (effectLower.includes("damage")) {
        return SKILL_ICONS["buff-damage"];
    } else if (effectLower.includes("cooldown")) {
        return SKILL_ICONS["buff-cooldown"];
    } else if (effectLower.includes("range") || effectLower.includes("radius")) {
        return SKILL_ICONS["buff-range"];
    } else if (effectLower.includes("duration")) {
        return SKILL_ICONS["buff-duration"];
    } else if (effectLower.includes("heal")) {
        return SKILL_ICONS["buff-healing"];
    } else if (effectLower.includes("defense") || effectLower.includes("shield") || effectLower.includes("absorption")) {
        return SKILL_ICONS["buff-defense"];
    } else if (effectLower.includes("speed") || effectLower.includes("movement")) {
        return SKILL_ICONS["buff-speed"];
    } else if (effectLower.includes("area") || effectLower.includes("aoe")) {
        return SKILL_ICONS["buff-area"];
    }
    
    // Default icon
    return {
        emoji: "‚ú®",
        cssClass: "icon-buff-generic",
        color: "#ffffff"
    };
}